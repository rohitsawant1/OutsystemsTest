                    using (var cmd = e.CreateCommand(con, P.SP("BEGIN TempGetAppId2({0}, {1}, {2}, {3}); END;", "appName", "appNameHash", "existingAppName", "existingAppNameHash"))) {
                        cmd.CommandTimeout = queryTimeout;
                        cmd.CommandType = CommandType.Text;
                        e.CreateParameter(cmd, P.P("appName"), DbType.String, str);
                        e.CreateParameter(cmd, P.P("appNameHash"), DbType.Int32, hash);
                        var existingAppName = e.CreateParameter(cmd, P.P("existingAppName"), DbType.String, DBNull.Value);
                        existingAppName.Size = SessionConstants.STORE_STR_MAX;
                        existingAppName.Direction = ParameterDirection.Output;
                        var existingAppNameHash = e.CreateParameter(cmd, P.P("existingAppNameHash"), DbType.Int32, DBNull.Value);
                        existingAppNameHash.Direction = ParameterDirection.Output;

                        cmd.ExecuteNonQuery();

                        if (existingAppName.Value == DBNull.Value) {
                            ehash = Convert.ToInt32(existingAppNameHash.Value);
                            collidedStr = null;
                            return true;
                        } else {
                            ehash = -1;
                            collidedStr = Convert.ToString(existingAppName.Value);
                            return false;
                        }
                    }
                }
            } catch (Exception e) {
                throw new SessionStoreException(e.Message, e);
            }
        }

