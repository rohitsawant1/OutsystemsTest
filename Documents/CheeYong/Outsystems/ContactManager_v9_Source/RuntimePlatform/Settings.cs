        private static readonly ConcurrentStack<SettingsStoreReader> settingReaderOverrides = new ConcurrentStack<SettingsStoreReader>();
        private static readonly ConcurrentStack<settingStoreWriter> settingWriterOverrides = new ConcurrentStack<settingStoreWriter>();

        public static void OverrideSettings(SettingsStoreReader ssr, settingStoreWriter ssw,
                                            ISettingsCache<string> settingsCache,
                                            ISettingsCache<int> settingsCacheInt,
                                            ISettingsCache<decimal> settingsCacheDecimal,
                                            ISettingsCache<bool> settingsCacheBool) {

            PushSettingsStoreOverride(ssr, ssw);
            PlatformSettings.settingsCache = settingsCache;
            PlatformSettings.settingsCacheInt = settingsCacheInt;
            PlatformSettings.settingsCacheDecimal = settingsCacheDecimal;
            PlatformSettings.settingsCacheBool = settingsCacheBool;
        }

        public static Dictionary<string, string> GetAllOverrides() {
            var result = new Dictionary<string, string>();
            var overrides = settingReaderOverrides.SafeToList();
            overrides.Reverse();
            overrides.Apply(settings => result.AddRangeReplacing(settings.GetAllOverrides()));

            return result;
        }

        public static void PushSettingsStoreOverride(SettingsStoreReader ssr, settingStoreWriter ssw) {
            settingReaderOverrides.Push(ssr);
            settingWriterOverrides.Push(ssw);
        }

        public static void PopSettingsStoreOverride() {
            if (!settingReaderOverrides.TryPop(out _)) {
                throw new InvalidOperationException($"{nameof(settingReaderOverrides)} Stack empty");
            }
            if (!settingWriterOverrides.TryPop(out _)) {
                throw new InvalidOperationException($"{nameof(settingWriterOverrides)} Stack empty");
            }
        }

