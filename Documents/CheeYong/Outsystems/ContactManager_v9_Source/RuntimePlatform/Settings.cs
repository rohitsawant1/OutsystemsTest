        private static string GetParameter(string name, bool isNodeSetting, Transaction transaction) {
            string result = null;
            int retries = 3;
            for (;;) {
                Transaction tran = (transaction ?? GetSystemTransaction());
                try {
                    object val = GetEnvironmentConfiguration(tran, name, isNodeSetting);

                    if (val != DBNull.Value) {
                        result = (string)val;
                    }
                } catch (Exception e) {
                    OSTrace.Error("Retrieving Parameter " + name, e);
                    if (retries-- == 0) {
                        OSTrace.Error("Failing on GetParameter('" + name + "') after using all retries");
                        throw;
                    } else {
                        continue;
                    }
                } finally {
                    if (transaction == null) {
                        tran.Dispose();
                    }
                }
                break;
            }
            return result;
        }
        public static object GetEnvironmentConfiguration(Transaction tran, string configKey, bool isNodeSetting) {
            string sql = "SELECT VAL FROM OSSYS_PARAMETER PARAMETER WHERE UPPER(NAME)=@NAME";
            if (isNodeSetting) {
                sql += " AND UPPER(HOST_SERIAL) = @HOSTSERIAL";
            }
            using (Command cmd = tran.CreateCommand(sql)) {
                cmd.CreateParameter("@NAME", DbType.String, configKey.ToUpperInvariant());
                if (isNodeSetting) {
                    cmd.CreateParameter("@HOSTSERIAL", DbType.String, RuntimePlatform.Internal.SettingsInternal.MachineSerial.ToUpperInvariant());
                }
                return cmd.ExecuteScalar(true);
            }
        }

