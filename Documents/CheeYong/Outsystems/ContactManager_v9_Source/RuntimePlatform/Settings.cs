        public interface ISettingsCache<TValue> {
            bool Contains(string key);
            bool Remove(string key);
            TValue Get(string key);
            TValue GetOrAdd(string key, Func<TValue> valueFactory);
            bool TryGetValue(string key, out TValue value);
            void Set(string key, TValue value);
            void Clear();
        }

        public class SettingsCache<TValue> : ISettingsCache<TValue>, IDisposable {
            private readonly ConcurrentDictionary<string, TValue> cache = new ConcurrentDictionary<string, TValue>();

            public bool Contains(string key) {
                return cache.ContainsKey(key);
            }

            public bool Remove(string key) {
                return ((IDictionary<string, TValue>)cache).Remove(key);
            }

            public TValue Get(string key) {
                TValue result;
                if (!cache.TryGetValue(key, out result)) {
                    return default(TValue);
                }

                return result;
            }

            public TValue GetOrAdd(string key, Func<TValue> valueFactory) {
                return cache.GetOrAdd(key, valueFactory);
            }

            public bool TryGetValue(string key, out TValue value) {
                return cache.TryGetValue(key, out value);
            }

            public void Set(string key, TValue value) {
                cache[key] = value;
            }

            public void Clear() {
                cache.Clear();
            }

            public void Dispose() {
            }
        }

