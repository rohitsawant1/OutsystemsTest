namespace OutSystems.RuntimeCommon.Utils {

    public static class ExceptionUtils {
        public static string GetExceptionStack(Exception ex) {
            string msg = string.Empty;
            int count = 1;
            while (ex != null) {
                if (count != 1) {
                    msg += Environment.NewLine;
                }
                string target = "NULL";
                if (ex.TargetSite != null) {
                    target = ex.TargetSite.DeclaringType.FullName + "." + ex.TargetSite.Name;
                }
                msg = string.Format("[{0}] {1}\nTarget : {2}\nSource : {3}\nMessage : {4}\nStack Trace\n{5}",
